import java.util.Scanner; //Scanner 클래스 소환

public class Solution {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in); //System.in: 입력 스트림
        
        int a = sc.nextInt();
        System.out.println("a = " + a);
        
        int b = sc.nextInt();
        System.out.println("b = " + b);
        //System.out: 출력 스트림
    }
}